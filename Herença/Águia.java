
public class Águia extends Animal {
	
	public Águia()
	{
		super("Águia");
	}
	@Override
	public void Nome (String nomeanimal)
	{
	System.out.println("Nome da águia é:"+ nomeanimal);
	}
	@Override
	public void Idade (int idadeanimal)
	{
		System.out.println("Idade da águia é:"+ idadeanimal);
	}
	@Override
	public void Som (String somanimal)
	{
		System.out.println("O som da águia é:"+ somanimal);
	}
	
	

}
